export function getDemoExploreMaterial(slug: string): ExploreMaterial | undefined {
  return DEMO_EXPLORE_MATERIALS.find((m) => m.slug === slug);
}

export function getDemoExploreMaterialsByCategory(
  category: string
): ExploreMaterial[] {
  return DEMO_EXPLORE_MATERIALS.filter((m) => m.category === category);
}

export function getFeaturedDemoMaterials(limit = 6): ExploreMaterial[] {
  return DEMO_EXPLORE_MATERIALS.filter((m) => m.isFeatured).slice(0, limit);
}
