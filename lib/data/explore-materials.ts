function material(
  data: Omit<ExploreMaterial, "id" | "content"> & {
    sections: Parameters<typeof lexicalFromSections>[0];
  }
): ExploreMaterial {
  const { sections, ...rest } = data;
  return {
    ...rest,
    id: `demo-${rest.slug}`,
    content: lexicalFromSections(sections),
  };
}

