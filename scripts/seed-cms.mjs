function geoToRoutePoints(geoLine) {
  if (!geoLine?.coordinates?.length) return [];
  return geoLine.coordinates.map(([lng, lat], i) => ({
    title: `Точка ${i + 1}`,
    lat,
    lng,
    order: i + 1,
    published: true,
    description: "",
  }));
}

